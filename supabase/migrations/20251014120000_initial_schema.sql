-- ----------------------------------------------------------------------------
-- Table: profiles
-- Purpose: Extends auth.users with application-specific user data
-- RLS: Enabled - users can only access their own profile
-- ----------------------------------------------------------------------------
create table public.profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  privacy_consent boolean not null default false,
  deleted_at timestamptz,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  
  -- Soft delete constraint: ensure deleted_at is in the past or null
  constraint valid_deleted_at check (deleted_at is null or deleted_at <= now())
);

comment on table public.profiles is 'User profile data extending Supabase Auth users';
comment on column public.profiles.id is 'References auth.users(id) - one-to-one relationship';
comment on column public.profiles.privacy_consent is 'User consent for AI processing of their data';
comment on column public.profiles.deleted_at is 'Soft delete timestamp - NULL means active account';

-- ----------------------------------------------------------------------------
-- Table: decks
-- Purpose: Stores flashcard deck information
-- RLS: Enabled - users can only access their own decks
-- ----------------------------------------------------------------------------
create table public.decks (
  id uuid primary key default gen_random_uuid(),
  user_id uuid not null references auth.users(id) on delete cascade,
  name varchar(255) not null,
  created_by_ai boolean not null default false,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  
  -- Business rule: deck name cannot be empty
  constraint non_empty_name check (trim(name) <> '')
);

comment on table public.decks is 'Flashcard decks owned by users';
comment on column public.decks.user_id is 'Owner of the deck';
comment on column public.decks.created_by_ai is 'Flag indicating if deck was generated by AI';

-- ----------------------------------------------------------------------------
-- Table: cards
-- Purpose: Individual flashcards with SM-2 spaced repetition algorithm data
-- RLS: Enabled - users can access cards from their own decks
-- ----------------------------------------------------------------------------
create table public.cards (
  id uuid primary key default gen_random_uuid(),
  deck_id uuid not null references public.decks(id) on delete cascade,
  question text not null,
  answer text not null,
  ease_factor decimal(3,2) not null default 2.50,
  interval_days integer not null default 1,
  repetitions integer not null default 0,
  next_review_date timestamptz not null default now(),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  
  -- SM-2 algorithm constraints
  constraint valid_ease_factor check (ease_factor between 1.30 and 2.50),
  constraint valid_interval_days check (interval_days >= 1),
  constraint valid_repetitions check (repetitions >= 0),
  
  -- Business rules: question and answer cannot be empty
  constraint non_empty_question check (trim(question) <> ''),
  constraint non_empty_answer check (trim(answer) <> ''),
  
  -- Length constraints as per schema (10000 chars)
  constraint question_length check (length(question) <= 10000),
  constraint answer_length check (length(answer) <= 10000)
);

comment on table public.cards is 'Flashcards with SM-2 spaced repetition algorithm data';
comment on column public.cards.ease_factor is 'SM-2 ease factor (1.30-2.50) - measures card difficulty';
comment on column public.cards.interval_days is 'SM-2 interval - days until next review';
comment on column public.cards.repetitions is 'SM-2 repetitions count - number of successful reviews';
comment on column public.cards.next_review_date is 'Scheduled date for next review';

-- ----------------------------------------------------------------------------
-- Table: reviews
-- Purpose: Historical record of card review sessions and grades
-- RLS: Enabled - users can only access their own reviews
-- ----------------------------------------------------------------------------
create table public.reviews (
  id uuid primary key default gen_random_uuid(),
  card_id uuid not null references public.cards(id) on delete cascade,
  user_id uuid not null references auth.users(id) on delete cascade,
  grade integer not null,
  review_date timestamptz not null default now(),
  
  -- SM-2 grade constraint (0-5)
  -- 0: complete blackout, 1: incorrect response with correct answer seemed familiar
  -- 2: incorrect response with correct answer seemed easy to recall
  -- 3: correct response recalled with serious difficulty
  -- 4: correct response after hesitation, 5: perfect response
  constraint valid_grade check (grade between 0 and 5)
);

comment on table public.reviews is 'Historical record of card reviews with SM-2 grades';
comment on column public.reviews.grade is 'SM-2 grade (0-5) where 0=total forget, 5=perfect recall';
comment on column public.reviews.review_date is 'Timestamp when review was completed';

-- ----------------------------------------------------------------------------
-- Table: ai_generation_logs
-- Purpose: Audit log for AI-generated flashcard operations
-- RLS: Enabled - users can only access their own logs
-- ----------------------------------------------------------------------------
create table public.ai_generation_logs (
  id uuid primary key default gen_random_uuid(),
  user_id uuid not null references auth.users(id) on delete cascade,
  deck_id uuid references public.decks(id) on delete set null,
  input_text_length integer not null,
  generated_cards_count integer not null default 0,
  error_message text,
  created_at timestamptz not null default now(),
  
  -- Business rules: lengths and counts must be non-negative
  constraint valid_input_length check (input_text_length >= 0),
  constraint valid_cards_count check (generated_cards_count >= 0)
);

comment on table public.ai_generation_logs is 'Audit log for AI flashcard generation operations';
comment on column public.ai_generation_logs.deck_id is 'Target deck (NULL if creating new deck)';
comment on column public.ai_generation_logs.error_message is 'Error details (NULL on success)';

