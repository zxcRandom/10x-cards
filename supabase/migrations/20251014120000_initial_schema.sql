-- ----------------------------------------------------------------------------
-- Function: update_updated_at_column
-- Purpose: Automatically update updated_at timestamp on row modification
-- Usage: Attached as BEFORE UPDATE trigger on tables with updated_at column
-- ----------------------------------------------------------------------------
create or replace function public.update_updated_at_column()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

comment on function public.update_updated_at_column is 'Trigger function to auto-update updated_at timestamp';

-- ----------------------------------------------------------------------------
-- Triggers: updated_at automation
-- ----------------------------------------------------------------------------

-- Trigger for profiles table
create trigger set_updated_at_profiles
  before update on public.profiles
  for each row
  execute function public.update_updated_at_column();

-- Trigger for decks table
create trigger set_updated_at_decks
  before update on public.decks
  for each row
  execute function public.update_updated_at_column();

-- Trigger for cards table
create trigger set_updated_at_cards
  before update on public.cards
  for each row
  execute function public.update_updated_at_column();

-- Note: reviews and ai_generation_logs tables don't have updated_at columns
-- as they are typically immutable (append-only logs)

-- ============================================================================
-- HELPER FUNCTION: Auto-create profile on user signup
-- ============================================================================

-- ----------------------------------------------------------------------------
-- Function: handle_new_user
-- Purpose: Automatically create a profile entry when a new user signs up
-- Trigger: After INSERT on auth.users
-- ----------------------------------------------------------------------------
create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  insert into public.profiles (id, privacy_consent, created_at, updated_at)
  values (new.id, false, now(), now());
  return new;
end;
$$;

comment on function public.handle_new_user is 'Auto-create profile on user signup';

-- Create trigger on auth.users table to auto-create profiles
create trigger on_auth_user_created
  after insert on auth.users
  for each row
  execute function public.handle_new_user();

